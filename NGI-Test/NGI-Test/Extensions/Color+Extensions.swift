import UIKit

extension UIColor {
    
    static func app_GreenButton() -> UIColor {
        return UIColor(named: "ABC") ?? .red
    }
    static func app_BlackLabel() -> UIColor {
        return UIColor(named: "ALC") ?? .red
    }
    static func app_YellowishColor() -> UIColor {
        return UIColor(named: "Yellowish") ?? .red
    }
}
